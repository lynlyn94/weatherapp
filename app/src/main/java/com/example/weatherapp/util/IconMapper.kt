package com.example.weatherapp.util

import com.example.weatherapp.R

object IconMapper {
    // 将 WeatherAPI 的 code 映射到我们现有的本地资源
    fun getWeatherIconResId(iconCode: String): Int {
        val code = iconCode.toIntOrNull() ?: 999
        return when (code) {
            // ===============================================
            // ☀️ 晴天 / 晴朗
            // ===============================================
            1000 -> R.drawable.ic_100

            // ===============================================
            // ⛅ 多云 (Partly cloudy)
            // ===============================================
            1003 -> R.drawable.ic_101

            // ===============================================
            // ☁️ 阴天 / 雾 / 霾 (Cloudy, Overcast, Mist, Fog)
            // ===============================================
            1006, 1009, 1030, 1135, 1147 -> R.drawable.ic_104

            // ===============================================
            // 🌧️ 雨 / 雷雨 / 冻雨 (Rain, Drizzle, Thunder, Sleet)
            // ===============================================
            // 1063(帕查雨), 1069(帕查雨夹雪), 1072(冻雨), 1087(雷暴)
            // 1150-1207(各种细雨和小雨), 1240-1252(阵雨)
            // 1273-1282(雷雨)
            1063, 1069, 1072, 1087,
            1150, 1153, 1168, 1171, 1180, 1183, 1186, 1189, 1192, 1195, 1198, 1201, 1204, 1207,
            1240, 1243, 1246, 1249, 1252,
            1273, 1276, 1279, 1282 -> R.drawable.ic_300

            // ===============================================
            // ❄️ 雪 / 暴雪 / 冰雹 (Snow, Blizzard, Ice pellets)
            // ===============================================
            // 1066(帕查雪), 1114-1117(吹雪/暴雪)
            // 1210-1237(各种雪), 1255-1264(阵雪)
            // 1261-1264(冰雹)
            1066, 1114, 1117,
            1210, 1213, 1216, 1219, 1222, 1225, 1237,
            1255, 1258, 1261, 1264 -> R.drawable.ic_400

            // ===============================================
            // ❓ 未知
            // ===============================================
            else -> R.drawable.ic_999
        }
    }
}